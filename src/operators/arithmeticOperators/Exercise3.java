package operators.arithmeticOperators;

public class Exercise3 {
    public static void main(String[] args) {

        double yearlySalary = 90_000.00; //90000.00
        double monthlySalary = yearlySalary / 12;
        double biWeeklySalary = yearlySalary / 26;
        double weeklySalary = yearlySalary / 52;

        System.out.println(" Average yearly salary is = " + yearlySalary);
        System.out.println(" Average monthly salary is = " + monthlySalary);
        System.out.println(" Average bi-weekly salary is = " + biWeeklySalary);
        System.out.println(" Average weekly salary is = " + weeklySalary);





    }
}
