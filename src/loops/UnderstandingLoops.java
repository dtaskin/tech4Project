package loops;

public class UnderstandingLoops {
    public static void main(String[] args) {
        /*

        Write a jva program to print "Hello" 5 times

        Expected
        Hello
        Hello
        Hello
        Hello
        Hello
         */

        for (int count = 1; count <= 5; count++) {
            System.out.println("Hello");
        }

    }



}
